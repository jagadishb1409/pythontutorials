fruits = ["apple", "banana", "orange"]
print(fruits[0]) # Output: "apple"

fruits = ("apple", "banana", "orange")
print(fruits[0]) # Output: "apple"

fruits = {"apple", "banana", "orange"}
print("banana" in fruits) # Output: True

fruits = {"apple": 1, "banana": 2, "orange": 3}
print(fruits["banana"]) # Output: 2
