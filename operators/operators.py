x = 5
y = 3

# addition
print(x + y) # Output: 8

# subtraction
print(x - y) # Output: 2

# multiplication
print(x * y) # Output: 15

# division
print(x / y) # Output: 1.6666667

# modulus
print(x % y) # Output: 2

# exponent
print(x ** y) # Output: 125

# floor division
print(x // y) # Output: 1


x = 5
y = 3

# equal to
print(x == y) # Output: False

# not equal to
print(x != y) # Output: True

# greater than
print(x > y) # Output: True

# less than
print(x < y) # Output: False

# greater than or equal to
print(x >= y) # Output: True

# less than or equal to
print(x <= y) # Output: False


x = True
y = False

